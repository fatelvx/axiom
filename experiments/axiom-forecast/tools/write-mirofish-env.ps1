param(
  [string]$MiroFishRoot,
  [switch]$TemplateOnly
)

$ErrorActionPreference = "Stop"

function Resolve-MiroFishRoot {
  param([string]$RequestedRoot)

  if ($RequestedRoot) {
    return (Resolve-Path -LiteralPath $RequestedRoot).Path
  }

  $candidates = Get-ChildItem -Path $env:TEMP -Directory -Filter "axiom-mirofish-*" -ErrorAction SilentlyContinue |
    Sort-Object LastWriteTime -Descending

  if (-not $candidates -or $candidates.Count -eq 0) {
    throw "MiroFish root was not provided and no <temp>/axiom-mirofish-* clone was found. Pass -MiroFishRoot <path>."
  }

  return $candidates[0].FullName
}

function Read-SecretText {
  param([string]$Prompt)

  $secure = Read-Host $Prompt -AsSecureString
  $ptr = [Runtime.InteropServices.Marshal]::SecureStringToBSTR($secure)
  try {
    return [Runtime.InteropServices.Marshal]::PtrToStringBSTR($ptr)
  } finally {
    [Runtime.InteropServices.Marshal]::ZeroFreeBSTR($ptr)
  }
}

function Read-TextWithDefault {
  param(
    [string]$Prompt,
    [string]$Default
  )

  $value = Read-Host "$Prompt [$Default]"
  if ([string]::IsNullOrWhiteSpace($value)) {
    return $Default
  }

  return $value.Trim()
}

$root = Resolve-MiroFishRoot -RequestedRoot $MiroFishRoot
$envPath = Join-Path $root ".env"

if ($TemplateOnly) {
  $content = @(
    "# MiroFish local credentials. Fill this file locally; do not commit it.",
    "LLM_API_KEY=",
    "LLM_BASE_URL=https://dashscope.aliyuncs.com/compatible-mode/v1",
    "LLM_MODEL_NAME=qwen-plus",
    "",
    "ZEP_API_KEY=",
    "",
    "# Optional stronger model for synthesis / high-cost simulation steps.",
    "LLM_BOOST_API_KEY=",
    "LLM_BOOST_BASE_URL=",
    "LLM_BOOST_MODEL_NAME=",
    ""
  )
  Set-Content -LiteralPath $envPath -Value $content -Encoding UTF8
  Write-Host "Wrote template .env to $envPath"
  exit 0
}

Write-Host "Writing MiroFish credentials to $envPath"
Write-Host "Secrets are written only to the local .env file. They are not printed back."

$llmApiKey = Read-SecretText "LLM_API_KEY"
$llmBaseUrl = Read-TextWithDefault "LLM_BASE_URL" "https://dashscope.aliyuncs.com/compatible-mode/v1"
$llmModelName = Read-TextWithDefault "LLM_MODEL_NAME" "qwen-plus"
$zepApiKey = Read-SecretText "ZEP_API_KEY"

$boostChoice = Read-Host "Configure optional boost model? (y/N)"
$useBoost = $boostChoice -match "^(y|yes)$"

$boostApiKey = ""
$boostBaseUrl = ""
$boostModelName = ""

if ($useBoost) {
  $boostApiKey = Read-SecretText "LLM_BOOST_API_KEY"
  $boostBaseUrl = Read-TextWithDefault "LLM_BOOST_BASE_URL" $llmBaseUrl
  $boostModelName = Read-TextWithDefault "LLM_BOOST_MODEL_NAME" $llmModelName
}

$content = @(
  "# MiroFish local credentials. Generated by Axiom forecast helper.",
  "# Keep this file local; do not commit or paste it into chat.",
  "LLM_API_KEY=$llmApiKey",
  "LLM_BASE_URL=$llmBaseUrl",
  "LLM_MODEL_NAME=$llmModelName",
  "",
  "ZEP_API_KEY=$zepApiKey",
  "",
  "# Optional stronger model for synthesis / high-cost simulation steps.",
  "LLM_BOOST_API_KEY=$boostApiKey",
  "LLM_BOOST_BASE_URL=$boostBaseUrl",
  "LLM_BOOST_MODEL_NAME=$boostModelName",
  ""
)

Set-Content -LiteralPath $envPath -Value $content -Encoding UTF8
Write-Host "MiroFish .env updated: $envPath"
